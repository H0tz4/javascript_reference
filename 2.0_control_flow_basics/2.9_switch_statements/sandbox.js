const grade = 'A';

switch (grade) {
  case 'A':
    console.log('You got a A!');
    break;
  case 'B':
    console.log('You got a B!');
    break;
  case 'C':
    console.log('You got a C!');
    break;
  case 'D':
    console.log('You got a D!');
    break;
  default:
    console.log("That's is not a valid grade.");
}